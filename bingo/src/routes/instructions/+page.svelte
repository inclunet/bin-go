    <h2>História</h2>

    <p>O Inclubingo é um jogo de bingo online desenvolvido por Leonardo e Camila, apresentadores do canal <a href="https://www.youtube.com/@Inclunet" target="_blank" role="button">Inclunet no YouTube</a>. Eles tiveram a ideia do jogo depois de uma experiência não muito boa em uma viagem de férias. Eles foram convidados para jogar bingo, mas perceberam que não seria possível para eles jogarem sozinhos. seria necessário pedir ajuda de alguém para marcar os números sorteados, o que os tornaria espectadores passivos do jogo.</p>
    
    <p>Leonardo começou a pensar em como poderia fazer um jogo de bingo totalmente acessível para pessoas com deficiência visual. Ele queria que o jogo fosse fácil de usar e que oferecesse a mesma experiência de jogo que as pessoas com visão têm.</p>
    
    <p>Em duas semanas, Leonardo desenvolveu o primeiro protótipo do Inclubingo e convidou seu amigo Edson Garcia para ajudar a melhorar o design e assim tornar o jogo inclusivo, permitindo que pessoas com e sem deficiência visual pudessem jogar juntos. O jogo foi um sucesso na festa junina do casal e por isso eles resolveram disponibilizar o jogo online para oferecer a mesma experiência a outras pessoas com deficiência visual.</p>
    
    <h2>Como jogar</h2>
    
    <p>Para jogar Inclubingo, você ou a pessoa que fará o sorteio e todos os jogadores precisarão de um dispositivo móvel ou computador com acesso à internet;</p>
    
    <p>Para iniciar o jogo, você deve acessar o site do Inclubingo <a href="https://bingo.inclunet.com.br/" role="button" target="_blank"> https://bingo.inclunet.com.br </a> e clicar no botão “Jogue agora” da página inicial;</p>
    
    <p>Se aparecer algum anúncio basta procurar e clicar no botão fechar anúncio. Os anúncios ajudam a pagar os custos de operação do jogo, infelizmente não existe algum outro tipo de patrocínio ou subsídio para manter este serviço no ar neste momento;</p>
    
    <p>Na página seguinte clique no botão “Nova Rodada” para criar uma nova rodada de bingo. Por padrão será criada uma rodada de 75 números, que representa um jogo de média duração, mas no futuro você poderá escolher o tamanho da rodada.</p>
    
    <p>A página seguinte é uma página de espera. Ela contém um QR-Code e um link para que os jogadores possam sortear uma cartela de bingo para participar do jogo e pode ser projetada em uma TV, computador ou projetor, de modo que todas as pessoas possam capturar o código com a câmera de seu dispositivo móvel. O link disponível na tela também pode ser copiado e compartilhado via whatsapp e outros aplicativos;</p>
    
    <p>Cada jogador precisará capturar o QR Code ou acessar o link compartilhado para gerar uma nova cartela.</p>
    
    <p>Após todos os jogadores terem capturado o QR Code ou acessado o link para sortear uma nova cartela, a pessoa que está sorteando os números do bingo deverá clicar no botão iniciar a rodada.</p>
    
    <p>O jogo começa com o sorteio do primeiro número. O número sorteado é exibido na tela do computador, TV ou projetor onde a cartela de sorteio está sendo exibida e também é exibido na cartela de cada jogador;</p>
    
    <p>Todas as cartelas do bingo vêm configuradas para jogar em modo automático, isso quer dizer que cada número sorteado será marcado automaticamente na cartela sem intervenção do jogador. Para jogar no modo tradicional, os jogadores podem tocar no botão seletor de modo do jogo, que normalmente está mostrando a palavra "automático". Quando o botão é tocado, ele muda para "manual".</p>
    
    <p>Se o jogador colocar a cartela em modo manual, ele mesmo deve marcar os números sorteados tocando nos números que ouvir (o jogo não permite marcar números que não foram sorteados ainda)</p>
    
    <p>Quando o jogador marcar uma linha, coluna ou diagonal de números ou preencher toda a cartela, um som será reproduzido contínuamente e o botão seletor de modo de jogo mudará para a palavra “Bingo!”. Isso significa que a pessoa ganhou o prêmio ou a rodada;</p>
    
    <p>Para parar de tocar o som continuamente basta tocar no botão bingo ou aguardar que seja sorteado um novo número;</p>
    
    <p>Quando a rodada terminar, na tela do sorteio, aparecerá um botão “Nova Rodada” que gera uma nova rodada e mostra novamente a tela de espera com o QR-Code para que novos jogadores possam sortear suas cartelas, os jogadores que já estavam jogando são automáticamente redirecionados para uma cartela da nova rodada.</p>
    
